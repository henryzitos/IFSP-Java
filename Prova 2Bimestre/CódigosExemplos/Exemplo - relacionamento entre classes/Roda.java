public class Roda {
    private int aro;
    private double calibragem;

    public Roda(int aro, double calibragem) {
        this.aro = aro;
        this.calibragem = calibragem;
    }

    public int getAro() {
        return aro;
    }

    public void setAro(int aro) {
        this.aro = aro;
    }

    public double getCalibragem() {
        return calibragem;
    }

    public void setCalibragem(double calibragem) {
        this.calibragem = calibragem;
    }
}
